import random

print(random.randint(5, 20))  # The maximum output number is 20, and the minimum output number is 5.
print(random.randrange(3, 10, 2))  # The maximum output number is 9, and the minimum output number is 3.
print(random.uniform(2.5, 5.5))  # The maximum output number is 5.468898546516328, and the minimum output number is 2.537519647870489.
print(random.randint(1, 100))